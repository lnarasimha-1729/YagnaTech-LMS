import College from '../db/models/College.js';
import Branch from '../db/models/Branch.js';
import { generateCollegeId, generateAccessKey } from '../utils/uidGeneration.js';
import {
  generateUniqueCollegeCode,
  isValidCollegeCode,
  normalizeCollegeCode,
} from '../utils/collegeCode.js';

// True when `code` already belongs to some other college. Used as the
// collision callback for generateUniqueCollegeCode.
async function collegeCodeExists(code) {
  const existing = await College.findOne({ where: { yagId: code } });
  return Boolean(existing);
}

// Add a new college. clgId is auto-generated by default but the caller may
// pass an explicit one (e.g. an institution code that needs to match an
// admin's college_id for the College Dashboard to aggregate against it).
export async function addCollege(req, res) {
  try {
    const { clgName, clgId: explicitClgId, clgAddress, orgId } = req.body;
    if (!clgName) {
      return res.status(400).json({ message: 'clgName is required' });
    }
    const clgId = explicitClgId ? String(explicitClgId).trim() : generateCollegeId();
    const accesskey = generateAccessKey();
    const existingClg = await College.findByPk(clgId);
    if (existingClg) {
      return res.status(409).json({ message: 'College with this ID already exists' });
    }
    // Deterministically derive the immutable 4-char college code from clgId,
    // resolving the rare hash collision against existing rows before insert.
    const yagId = await generateUniqueCollegeCode(clgId, collegeCodeExists);
    const newClg = await College.create({ clgId, yagId, clgName, accesskey, clgAddress, orgId });
    res.status(201).json(newClg);
  } catch (error) {
    console.error('Error adding college:', error);
    res.status(500).json({ message: 'Internal server error' });
  }
}

// Resolve a college from its 4-char code. Public building block for signup:
// a student enters their college code and we return the matching college.
// 400 for a malformed code, 404 when no college owns it.
export async function getCollegeByCode(req, res) {
  try {
    const code = normalizeCollegeCode(req.params.code);
    if (!isValidCollegeCode(code)) {
      return res.status(400).json({ message: 'Invalid college code. Expected 4 characters [A-Z0-9].' });
    }
    const college = await College.findOne({
      where: { yagId: code },
      attributes: ['clgId', 'yagId', 'clgName', 'clgAddress', 'orgId', 'branchIds'],
    });
    if (!college) {
      return res.status(404).json({ message: 'No college found for this code' });
    }
    res.json(college);
  } catch (error) {
    console.error('Error fetching college by code:', error);
    res.status(500).json({ message: 'Internal server error' });
  }
}

